# if else statement 

# python uses four spaces to denote code blocks 
# rather than curly brackets like c languages and javascript

x = 1

if x == 1:
    print("x is 1.")
else:
    print("x is not 1.")

# consecutive lines with the same indentation
# group code into blocks. All code in a block
# must have the same indentation

y = 2

if y == 1:
    print("y")
    print("is")
    print("1")
elif y == 2:
    print("y")
    print("is")
    print("2")
elif y == 3:
    print("y")
    print("is")
    print("3")
else:
    print("y")
    print("is")
    print("not")
    print("1, 2, or 3")

# There can be zero or more elif parts and the else if optional