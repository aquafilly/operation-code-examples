# python types - Lists

var_list = ['abcd', 786, 2.23, 'john', 70.2]
var_tiny_list = [123, 'john']

print(var_list) # prints complete list

print(var_list[0]) # prints first element of the list

print(var_list[1:3]) # prints elements starting from 2nd to 4th

print(var_list[2:]) # prints eleemnts starting from 3rd element

print(var_tiny_list * 2) # prints list two times

print(var_list + var_tiny_list) # prints concatenated list
